# ---------- 占卜引擎 ----------
class OracleEngine:
    """贞人占卜引擎"""

    def __init__(self, db_path=None):
        if db_path is None:
            db_path = os.path.join(
                os.path.dirname(os.path.abspath(__file__)), "divination_history.db"
            )
        self.db_path = db_path
        self._init_db()

    def _init_db(self):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute(
                """CREATE TABLE IF NOT EXISTS history (
                    id INTEGER PRIMARY KEY AUTOINCREMENT,
                    question TEXT NOT NULL,
                    crack_pattern TEXT NOT NULL,
                    oracle_text TEXT NOT NULL,
                    explanation TEXT NOT NULL,
                    created_at TEXT NOT NULL
                )"""
            )

    def divine(self, question: str) -> dict:
        """执行一次占卜，返回兆纹、卦辞、解读"""
        category = classify_question(question)

        crack = random.choice(CRACK_TEMPLATES.get(category, CRACK_TEMPLATES["日常"]))
        oracle = random.choice(ORACLE_TEXTS.get(category, ORACLE_TEXTS["日常"]))
        explanation = random.choice(EXPLANATIONS.get(category, EXPLANATIONS["日常"]))

        result = {
            "crack_pattern": crack,
            "oracle_text": oracle,
            "explanation": explanation,
        }

        # 存入历史
        self._save(question, result)
        return result

    def _save(self, question: str, result: dict):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute(
                "INSERT INTO history (question, crack_pattern, oracle_text, explanation, created_at) "
                "VALUES (?, ?, ?, ?, ?)",
                (
                    question,
                    result["crack_pattern"],
                    result["oracle_text"],
                    result["explanation"],
                    datetime.now().strftime("%Y-%m-%d %H:%M"),
                ),
            )

    def get_history(self, limit: int = 50) -> list:
        """获取历史记录，按时间倒序"""
        with sqlite3.connect(self.db_path) as conn:
            cur = conn.execute(
                "SELECT id, question, crack_pattern, oracle_text, explanation, created_at "
                "FROM history ORDER BY id DESC LIMIT ?",
                (limit,),
            )
            return cur.fetchall()

    def delete_record(self, record_id: int):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute("DELETE FROM history WHERE id = ?", (record_id,))


