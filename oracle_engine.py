def classify_question(question: str) -> str:
    """根据关键词返回类别"""
    scores = {}
    for cat, keywords in CATEGORY_KEYWORDS.items():
        score = sum(1 for kw in keywords if kw in question)
        if score > 0:
            scores[cat] = score
    if not scores:
        return "日常"
    return max(scores, key=scores.get)


