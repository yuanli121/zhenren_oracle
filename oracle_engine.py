# ---------- 加权随机抽取 ----------
def _pick_tiered(templates, category):
    """从三级稀有度模板中加权抽取"""
    cat_data = templates.get(category, templates["日常"])
    tier = random.choices(TIER_NAMES, weights=TIER_WEIGHTS, k=1)[0]
    return tier, random.choice(cat_data[tier])


# ---------- 占卜引擎 ----------
class OracleEngine:
    """贞人占卜引擎 — 三级稀有度加权分布"""

    def __init__(self, db_path=None):
        if db_path is None:
            db_path = os.path.join(
                os.path.dirname(os.path.abspath(__file__)), "divination_history.db"
            )
        self.db_path = db_path
        self._init_db()

    def _init_db(self):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute(
                """CREATE TABLE IF NOT EXISTS history (
                    id INTEGER PRIMARY KEY AUTOINCREMENT,
                    question TEXT NOT NULL,
                    crack_pattern TEXT NOT NULL,
                    oracle_text TEXT NOT NULL,
                    explanation TEXT NOT NULL,
                    rarity TEXT NOT NULL DEFAULT 'common',
                    created_at TEXT NOT NULL
                )"""
            )
            # 兼容旧表：若无 rarity 列则添加
            cur = conn.execute("PRAGMA table_info(history)")
            cols = [row[1] for row in cur.fetchall()]
            if "rarity" not in cols:
                conn.execute("ALTER TABLE history ADD COLUMN rarity TEXT NOT NULL DEFAULT 'common'")

    def divine(self, question: str) -> dict:
        """执行一次占卜，三级稀有度加权随机"""
        category = classify_question(question)

        tier_crack, crack = _pick_tiered(CRACK_TEMPLATES, category)
        tier_oracle, oracle = _pick_tiered(ORACLE_TEXTS, category)
        tier_explain, explanation = _pick_tiered(EXPLANATIONS, category)

        # 取最高稀有度作为本次占卜评级
        tier_rank = {"common": 0, "rare": 1, "legendary": 2}
        best_tier = max([tier_crack, tier_oracle, tier_explain], key=lambda t: tier_rank[t])

        result = {
            "crack_pattern": crack,
            "oracle_text": oracle,
            "explanation": explanation,
            "rarity": TIER_LABELS[best_tier],
        }
        self._save(question, result)
        return result

    def _save(self, question: str, result: dict):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute(
                "INSERT INTO history (question, crack_pattern, oracle_text, explanation, rarity, created_at) "
                "VALUES (?, ?, ?, ?, ?, ?)",
                (
                    question,
                    result["crack_pattern"],
                    result["oracle_text"],
                    result["explanation"],
                    result["rarity"],
                    datetime.now().strftime("%Y-%m-%d %H:%M"),
                ),
            )

    def get_history(self, limit: int = 50) -> list:
        with sqlite3.connect(self.db_path) as conn:
            cur = conn.execute(
                "SELECT id, question, crack_pattern, oracle_text, explanation, rarity, created_at "
                "FROM history ORDER BY id DESC LIMIT ?",
                (limit,),
            )
            return cur.fetchall()

    def delete_record(self, record_id: int):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute("DELETE FROM history WHERE id = ?", (record_id,))

    def get_stats(self) -> dict:
        """统计各稀有度出现次数"""
        with sqlite3.connect(self.db_path) as conn:
            cur = conn.execute(
                "SELECT rarity, COUNT(*) FROM history GROUP BY rarity"
            )
            return dict(cur.fetchall())


