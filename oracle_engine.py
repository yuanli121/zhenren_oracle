# ---------- 加权随机抽取 ----------
def _pick_tiered(templates, category):
    """从六级吉凶模板中加权抽取"""
    cat_data = templates.get(category, templates["日常"])
    level = random.choices(AUSPICE_NAMES, weights=AUSPICE_WEIGHTS, k=1)[0]
    return level, random.choice(cat_data[level])


# ---------- 占卜引擎 ----------
class OracleEngine:
    """爻一摇引擎 — 六级吉凶加权分布"""

    def __init__(self, db_path=None):
        if db_path is None:
            db_path = os.path.join(
                os.path.dirname(os.path.abspath(__file__)), "divination_history.db"
            )
        self.db_path = db_path
        self._init_db()

    def _init_db(self):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute(
                """CREATE TABLE IF NOT EXISTS history (
                    id INTEGER PRIMARY KEY AUTOINCREMENT,
                    question TEXT NOT NULL,
                    crack_pattern TEXT NOT NULL,
                    oracle_text TEXT NOT NULL,
                    explanation TEXT NOT NULL,
                    auspice TEXT NOT NULL DEFAULT '无咎',
                    created_at TEXT NOT NULL
                )"""
            )
            cur = conn.execute("PRAGMA table_info(history)")
            cols = [row[1] for row in cur.fetchall()]
            if "auspice" not in cols:
                conn.execute("ALTER TABLE history ADD COLUMN auspice TEXT NOT NULL DEFAULT '无咎'")
            # 许可配置表
            conn.execute(
                """CREATE TABLE IF NOT EXISTS license (
                    key TEXT PRIMARY KEY,
                    value TEXT NOT NULL
                )"""
            )
            conn.execute(
                "INSERT OR IGNORE INTO license (key, value) VALUES ('usage_count', '0')"
            )
            conn.execute(
                "INSERT OR IGNORE INTO license (key, value) VALUES ('activated', '0')"
            )

    # ---------- 许可方法 ----------
    def _get_license(self, key: str) -> str:
        with sqlite3.connect(self.db_path) as conn:
            cur = conn.execute("SELECT value FROM license WHERE key = ?", (key,))
            row = cur.fetchone()
            return row[0] if row else "0"

    def _set_license(self, key: str, value: str):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute("UPDATE license SET value = ? WHERE key = ?", (value, key))

    def get_usage_count(self) -> int:
        return int(self._get_license("usage_count"))

    def get_free_uses_left(self) -> int:
        return max(0, FREE_LIMIT - self.get_usage_count())

    def is_activated(self) -> bool:
        return self._get_license("activated") == "1"

    def increment_usage(self):
        count = self.get_usage_count() + 1
        self._set_license("usage_count", str(count))

    def verify_code(self, code: str) -> bool:
        """签名验证激活码"""
        if check_activation_code(code):
            self._set_license("activated", "1")
            return True
        return False

    def _extract_concern(self, question: str) -> str:
        """从问题中提取核心关切，用于个性化前缀"""
        cleaned = question.strip().rstrip('?？!！。.,，')
        for p in ['吗', '呢', '啊', '吧', '呀', '哦', '嘛', '呗', '能不能', '会不会', '可不可以']:
            cleaned = cleaned.replace(p, '')
        cleaned = cleaned.strip()
        if len(cleaned) > 16:
            cleaned = cleaned[:16] + '…'
        return cleaned

    def _personalize(self, question: str, auspice: str, explanation: str) -> str:
        """在解释前加入呼应具体问题的个性化导语"""
        concern = self._extract_concern(question)
        if not concern:
            return explanation
        lead_ins = {
            "大吉": f"「{concern}」——天地以极吉之兆回应汝问：",
            "吉": f"「{concern}」——龟兆光明，天地答曰：",
            "贞吉": f"「{concern}」——兆纹昭然，其意曰：",
            "无咎": f"「{concern}」——天地沉吟片刻，答曰：",
            "悔吝": f"「{concern}」——龟兆微蹙，天地劝曰：",
            "厉": f"「{concern}」——此问触动天机，龟兆示警：",
        }
        prefix = lead_ins.get(auspice, f"「{concern}」——天地答曰：")
        return prefix + explanation

    def divine(self, question: str) -> dict:
        """执行一次占卜，六级吉凶加权随机"""
        category = classify_question(question)

        lev_crack, crack = _pick_tiered(CRACK_TEMPLATES, category)
        lev_oracle, oracle = _pick_tiered(ORACLE_TEXTS, category)
        lev_explain, explanation = _pick_tiered(EXPLANATIONS, category)

        # 取最高吉凶等级
        level_order = {name: i for i, name in enumerate(AUSPICE_NAMES)}
        best_level = max([lev_crack, lev_oracle, lev_explain], key=lambda t: level_order[t])

        # 个性化解释：呼应具体问题
        explanation = self._personalize(question, best_level, explanation)

        result = {
            "crack_pattern": crack,
            "oracle_text": oracle,
            "explanation": explanation,
            "auspice": best_level,
            "auspice_label": AUSPICE_LABELS[best_level],
        }
        self._save(question, result)
        return result

    def _save(self, question: str, result: dict):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute(
                "INSERT INTO history (question, crack_pattern, oracle_text, explanation, auspice, created_at) "
                "VALUES (?, ?, ?, ?, ?, ?)",
                (
                    question,
                    result["crack_pattern"],
                    result["oracle_text"],
                    result["explanation"],
                    result["auspice_label"],
                    datetime.now().strftime("%Y-%m-%d %H:%M"),
                ),
            )

    def get_history(self, limit: int = 50) -> list:
        with sqlite3.connect(self.db_path) as conn:
            cur = conn.execute(
                "SELECT id, question, crack_pattern, oracle_text, explanation, auspice, created_at "
                "FROM history ORDER BY id DESC LIMIT ?",
                (limit,),
            )
            return cur.fetchall()

    def delete_record(self, record_id: int):
        with sqlite3.connect(self.db_path) as conn:
            conn.execute("DELETE FROM history WHERE id = ?", (record_id,))

    def get_stats(self) -> dict:
        """统计各吉凶等级出现次数"""
        with sqlite3.connect(self.db_path) as conn:
            cur = conn.execute("SELECT auspice, COUNT(*) FROM history GROUP BY auspice")
            return dict(cur.fetchall())


